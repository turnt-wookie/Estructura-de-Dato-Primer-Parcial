  /*
   * Metodo que llama a los ordenamientos 
   */  
    private void agrup1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agrup1ActionPerformed
        Date dateInicio= new Date();
        //Se consigue el index del combobox para saber que pagina se leera.
        int a = crit.getSelectedIndex();
       
        String[] x,y;
        double[] z;
        
        //Se llama al metodo leer del controlador y se le manda la pagina que se leera.
        cr.leer(a);
        //Se llama al metodo metodo del controlador y se le manda un entero que le dira que metodo de ordenacion usar.
        if(ori.getSelectedIndex()==0){
            cr.metodo(me);
        }
        else{
            cr.metodo(me+7);
        }
        
        //Ya hecho lo anterior, se consiguen los arreglo ordenados.
        x=cr.getA();
        y=cr.getB();
        z=cr.getC();

        
        //Se rellena el grid con los datos guardados en los arreglos.
        for(int i = 1;i<x.length;i++){
            tab.setValueAt(x[i], i-1, 0);
            tab.setValueAt(y[i], i-1, 1);
            tab.setValueAt(z[i], i-1, 2);
        }
        //Se descactiva el boton de agrupar para forzar al usuario a limpiar.        
        agrup1.setEnabled(false);
        Date dateFin= new Date();

        SimpleDateFormat sdf = new SimpleDateFormat("ss");
        JOptionPane.showMessageDialog(null, "El tiempo de ejecucion fue de: " + sdf.format(dateFin.getTime()-dateInicio.getTime()) + " Segundos");
        JOptionPane.showMessageDialog(null, "Si utilizaste Mezclas los archivos quedaron en la Unidad C de tu Equipo.");

        
        
    }//GEN-LAST:event_agrup1ActionPerformed

